export type JobTag = (typeof JOB_TAGS)[number];

const JOB_TAG_SET = new Set<string>(JOB_TAGS);

const JOB_TAG_LOOKUP = new Map<string, string>(
  JOB_TAGS.map((tag) => [tag.toLowerCase(), tag]),
);

export function jobTagLabel(tag: string) {
  return tag.replace(/^#/, "");
}

export function resolveJobTag(tag: string): JobTag | null {
  const trimmed = tag.trim();
  if (!trimmed) return null;
  const normalized = trimmed.replace(/^#/, "");
  if (JOB_TAG_SET.has(normalized)) return normalized as JobTag;
  return (JOB_TAG_LOOKUP.get(normalized.toLowerCase()) as JobTag | undefined) ?? null;
}

export function resolveJobTags(tags: string[]): JobTag[] {
  const out: JobTag[] = [];
  for (const tag of tags) {
    const resolved = resolveJobTag(tag);
    if (resolved && !out.includes(resolved)) out.push(resolved);
  }
  return out;
}
